#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.resolutions.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ResolutionCell" forIndexPath:indexPath];
    
    // Configure the cell...
    NSDictionary * resolutionDict = self.resolutions[indexPath.row];
    cell.textLabel.text = resolutionDict[@"resolution"];
    if (indexPath.row == self.selectionIndex) {
        cell.accessoryType = UITableViewCellAccessoryCheckmark;
    } else {
        cell.accessoryType = UITableViewCellAccessoryNone;
    }
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    //[self.delegate camerasViewController:self didSelectCamera:self.cameras[indexPath.row]];
    [self.delegate resolutionSelector:self didSelectResolutionAtIndex:indexPath.row];
    [self dismissViewControllerAnimated:YES completion:^{
        
    }];
}

